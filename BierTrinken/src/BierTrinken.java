/*
 * Einfaches Beispiel für eine for- oder Zählschleife
 * Die geschweiften Klammern um den Block dürfen entfallen,
 * wenn - wie hier - der Block nur aus einer Anweisung besteht.
 */

public class BierTrinken {

	public static void main(String[] args) {
		
		for(int striche=0; striche<5; striche = striche+1) 
			System.out.println("Ich trinke ein Bier!");
		
	}
}
