/*
 * Übung für Escape-Sequenzen: \n u. \t
 */

public class Tabelle {

	public static void main(String[] args) {
		System.out.println("\nDies ist eine Tabelle:\n");
		System.out.println("\t\tSpalte 1\t\tSpalte 2\t\tSpalte 3\n");
		System.out.println("Zeile 1:\t\t3\t\t5500\t\t4");
		System.out.println("Zeile 2:\t\t987\t\t85\t\t3");
		System.out.println("Zeile 3:\t\t9999\t\t58\t\t576");
	}
}
